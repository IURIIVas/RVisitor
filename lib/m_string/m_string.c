/// \brief Compare two strings
/// \param char* x - first string; char* y - second string
/// \retval int8
/// \return 1 if x == y, else 0
int8_t m_strcmp(const char *x, const char *y)
{
	while (*x)
	{
		if (*x != *y)
		{
			return 0;
		}

		x++;
		y++;
	}
	return 1;
}
